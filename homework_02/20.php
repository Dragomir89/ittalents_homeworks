<?php
// Задача 20:
// Да се състави програма, чрез която се извежда квадрат от
// цифри. Сумите от елементите на произволен ред или стълб са
// равни на 45





for ($i = 0; $i < 10; $i++) {
	$count = 0;
	$line = '';
	
	for ($j = $i; $j < 10; $j++) {
		
		$line .= "$j ";
		$count ++;
		
	}
	
	
	$count = 10 - $count;
	
	
	
	for ($k = 0; $k < $count; $k++) {
		$line .= "$k ";
	}
	
	echo $line;
	
	
	echo PHP_EOL;
	
}
